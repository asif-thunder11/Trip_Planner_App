package org.thundercorp.tripplanner.DataModels;

public class ExpenseItem {

    String user, name, category;
    int amount;

    public String getUser() {
        return user;
    }

    public String getName() {
        return name;
    }

    public String getCategory() {
        return category;
    }

    public int getAmount() {
        return amount;
    }
}
